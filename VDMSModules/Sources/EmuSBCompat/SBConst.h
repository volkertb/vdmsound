/////////////////////////////////////////////////////////////////////////////
// I/O
/////////////////////////////////////////////////////////////////////////////

#define SB_STATUS_RW    (1 << 7)
#define SB_STATUS_PAD   (0xff & (~SB_STATUS_RW))

#define MK_SB_RD_STATUS(ready)\
(((ready) ? SB_STATUS_RW : 0) | SB_STATUS_PAD)

#define MK_SB_WR_STATUS(ready)\
(((ready) ? 0 : SB_STATUS_RW) | SB_STATUS_PAD)

